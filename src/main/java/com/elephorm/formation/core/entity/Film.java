package com.elephorm.formation.core.entity;

public class Film {
	private String titre;
	private String genre;
	private int nbreExplaire;

	public String getTitre() {
		return titre;
	}

	public void setTitre(String titre) {
		this.titre = titre;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

	public int getNbreExplaire() {
		return nbreExplaire;
	}

	public void setNbreExplaire(int nbreExplaire) {
		this.nbreExplaire = nbreExplaire;
	}

}
